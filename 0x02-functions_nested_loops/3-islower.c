#include "main.h"

/**
 * _islower(int c) - checks for lowercase characters
 * and returns 1 if lowercase, and 0 otherwise
 *
 * Description: This function checks for lowercase characters
 * main - Entry point
 * Return: Always 0 (Success)
*/

int _islower(int c)
{
int r;
r = _islower(c);
_putchar(r + '0');
_putchar('\n');
return(0);
}
