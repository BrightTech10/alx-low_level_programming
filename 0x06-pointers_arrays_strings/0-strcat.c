#include "main.h"
#include <string.h>

/**
 * _strcat - concatenates two strings
 *
 * @dest: destination string
 * @src: source string; to be appended to the destination string
 * Return: returns pointer to destination string
*/

char *_strcat(char *dest, char *src)
{
strcat(dest, src);

return (dest);
}
